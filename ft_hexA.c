#include "ft_printf.h"

int ft_hexA(int n)
{
	char	c;
	int		i;
	
	i = 0;
	if (n >= 0 && n < 16)
	{
		if (n < 10)
			c = n + '0';
		else
		c = n - 10 + 'A';
	write(1, &c, 1);
	i++;
	}
	else
	{
		i += ft_hexA(n / 16);
		i += ft_hexA(n % 16);
	}
	return (i);
}
